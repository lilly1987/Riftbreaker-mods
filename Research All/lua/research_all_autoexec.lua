local GetResearch=function(research)
	PlayerService:EnableResearch( research )
	PlayerService:UnlockResearch( research )
end

local Already=false

local mainRun= function(evt)
	if ( Already == true ) then
		return
    end
	Already=true
	-----------------------------------------------
	
	local CurrentCampaignType=CampaignService:GetCurrentCampaignType()
	LogService:Log("CampaignType : " .. CurrentCampaignType )
    if CurrentCampaignType ~= "survival" then
        return
    end
	-----------------------------------------------
	local database1 = CampaignService:GetCampaignData()
    if ( database1 == nil ) then
        LogService:Log("NOT EXISTS database ")
        return
    end
	local k1="research_all_autoexec.lua/" 
	if database1:HasInt( k1) then
		LogService:Log(" Already applied " )
		return
	else
		database1:SetInt( k1,1)
		LogService:Log(" database set " )
	end
	-----------------------------------------------
	--local items = PlayerService:GetResearchesAvailableToUnlockList( true )
	--local items = PlayerService:GetResearchesAvailableToUnlockList( false )
	local items = list
	for _, item in ipairs(items) do		
		LogService:Log(" Research : " .. item )
		GetResearch(item)
	end
	
	--RegisterGlobalEventHandler("MissionFlowDeactivatedEvent",mainRun )
end

